use std::fs;
use std::path::Path;

fn ensure_default_icon() {
  let icon_path = Path::new("icons/icon.png");

  if icon_path.exists() {
    return;
  }

  if let Some(parent) = icon_path.parent() {
    fs::create_dir_all(parent).expect("failed to create icons directory");
  }

  // 1x1 PNG transparente para evitar panic do macro do Tauri quando o ícone
  // ainda não foi provisionado no projeto.
  const PNG_1X1_TRANSPARENT: &[u8] = &[
    0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, 0x00, 0x00, 0x00, 0x0D,
    0x49, 0x48, 0x44, 0x52, 0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01,
    0x08, 0x06, 0x00, 0x00, 0x00, 0x1F, 0x15, 0xC4, 0x89, 0x00, 0x00, 0x00,
    0x0D, 0x49, 0x44, 0x41, 0x54, 0x78, 0x9C, 0x63, 0x60, 0x60, 0x60, 0xF8,
    0x0F, 0x00, 0x01, 0x04, 0x01, 0x00, 0x70, 0x20, 0x65, 0x0B, 0x00, 0x00,
    0x00, 0x00, 0x49, 0x45, 0x4E, 0x44, 0xAE, 0x42, 0x60, 0x82,
  ];

  fs::write(icon_path, PNG_1X1_TRANSPARENT).expect("failed to write default icon file");
}

fn main() {
  ensure_default_icon();
  tauri_build::build();
}
